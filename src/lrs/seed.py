# Fixed so two runs produce identical data. With MERGE on the §6.3 keys that makes the
# seeder idempotent: a re-run converges instead of duplicating.
RNG_SEED = 20260716

TERM_START = datetime(2025, 9, 2, tzinfo=UTC)
TERM_END = datetime(2025, 12, 19, tzinfo=UTC)

# The real taxonomy categories are per-book subject areas ("Cellular Energetics"),
# not a cognitive scale, so they cannot pick a Bloom level. Depth in the DAG can:
# a concept with many prerequisites behind it is asking for more than recall. This is
# a heuristic for demo colour, not a claim about the source material.
BLOOM_BY_DEPTH = ["remember", "understand", "apply", "analyze", "evaluate", "create"]

VERBS = [
    ("experienced", "http://adlnet.gov/expapi/verbs/experienced", "experienced"),
    ("answered", "http://adlnet.gov/expapi/verbs/answered", "answered"),
    ("attempted", "http://adlnet.gov/expapi/verbs/attempted", "attempted"),
    ("completed", "http://adlnet.gov/expapi/verbs/completed", "completed"),
    ("interacted", "http://adlnet.gov/expapi/verbs/interacted", "interacted"),
    ("mastered", "http://adlnet.gov/expapi/verbs/mastered", "mastered"),
]

# (label, share of cohort, ability mean, ability sd, term progress mean)
# Ability is P(success) on a concept whose prerequisites all went perfectly; the DAG
# walk in _mastery_for_student takes it down from there. Progress is the fraction of
# the book reached by the end of the simulated term — nobody finishes, which is both
# realistic and what makes "what's next for this learner" a question worth asking.
ARCHETYPES = [
    ("struggling", 0.22, 0.44, 0.09, 0.42),
    ("typical", 0.54, 0.68, 0.09, 0.56),
    ("advanced", 0.24, 0.88, 0.06, 0.72),
]

# Share of learners carrying an explicit prerequisite gap.
GAP_SHARE = 0.35

# Rows per write transaction. Each ConceptMastery row is one MERGE plus three MATCHes
# and three relationship MERGEs, so a transaction costs far more than the row payload
# suggests. 5000 overruns dbms.memory.transaction.total.max (1.4 GiB, derived from the
# 2G heap §6.3 sets) once the catalogue passes ~500k summary vertices. 1000 keeps peak
# transaction memory flat; the extra round trips cost seconds, not minutes.
WRITE_BATCH = 1000


