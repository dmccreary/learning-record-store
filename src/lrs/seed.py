def clear(cfg: Settings) -> int:
    """Remove everything this seeder wrote, and nothing else.

    `seeded: true` is what makes this safe: a real projection written by the summarizer
    carries no such property and survives.

    CALL ... IN TRANSACTIONS, not a client-side batch loop. The whole delete is one
    logical statement, but Neo4j commits it every 1000 rows, so peak transaction memory
    stays flat no matter how large the dataset. A loop of `LIMIT 10000 DETACH DELETE`
    does NOT: each batch is one transaction, and on the full catalogue that blows
    dbms.memory.transaction.total.max (1.4 GiB, derived from the 2G heap §6.3 sets)
    before it finishes. This needs an autocommit session — IN TRANSACTIONS is illegal
    inside the managed transaction execute_query opens.
    """
    with _driver(cfg) as driver, driver.session() as session:
        result = session.run(
            "MATCH (n {seeded: true}) CALL (n) { DETACH DELETE n } IN TRANSACTIONS OF 1000 ROWS"
        )
        return int(result.consume().counters.nodes_deleted)


def build(profile: Profile, books: list[Book]) -> dict[str, Any]:
    """All rows for a profile. Pure — no I/O, so it is testable without a database."""
    rng = random.Random(RNG_SEED)
    content = _content(rng, books)
    tenancy = _tenancy(rng, profile, books)
    data: dict[str, Any] = {**content, **tenancy}
    data.update(_summaries(rng, content, tenancy, books))
    return data


def load_books(cfg: Settings, profile: Profile) -> list[Book]:
    """The catalogue this profile wants, from cfg.textbook_root."""
    root = Path(cfg.textbook_root).expanduser().resolve()
    books = load_catalog(root, only=list(profile.books) if profile.books else None)
    if not books:
        raise FileNotFoundError(
            f"no textbook learning graphs under {root}. Point LRS_TEXTBOOK_ROOT at the "
            f"directory holding the textbook repos (each needs "
            f"docs/learning-graph/learning-graph.csv)."
        )
    if profile.books:
        missing = set(profile.books) - {b["textbook_id"].removeprefix("tb-") for b in books}
        if missing:
            log.warning("profile %s wanted books not found: %s", profile.name, sorted(missing))
    log.info(
        "loaded %d textbooks from %s (%d concepts)",
        len(books),
        root,
        sum(len(b["concepts"]) for b in books),
    )
    return books


def seed(cfg: Settings, profile: Profile) -> dict[str, int]:
    """Write the demo graph. Idempotent — MERGE on the §6.3 keys."""
    data = build(profile, load_books(cfg, profile))
    counts: dict[str, int] = {}

    with _driver(cfg) as driver:
        for key, label, cypher in _WRITES:
            rows = data[key]
            for i in range(0, len(rows), WRITE_BATCH):
                driver.execute_query(cypher, rows=rows[i : i + WRITE_BATCH])
            counts[label] = len(rows)
            log.info("seeded %7d %s", counts[label], label)

        for i in range(0, len(data["covers"]), WRITE_BATCH):
            driver.execute_query(_COVERS, rows=data["covers"][i : i + WRITE_BATCH])
        counts["COVERS edges"] = len(data["covers"])
        log.info("seeded %7d %s", counts["COVERS edges"], "COVERS edges")

    return counts
