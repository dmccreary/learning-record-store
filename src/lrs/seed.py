"""`lrs seed` — demo districts, textbooks, and learners. Design §8.7.

"A stack you have to hand-populate before you can see anything is a stack nobody
explores" (design line 1155). Course content is IMPORTED from the real intelligent
textbook repos (see catalog.py); this module is the generator and the writer.

TWO PROFILES
------------
`--demo`     §8.7's shape: one district, two schools, four sections, two books.
`--showcase` Eight districts, forty schools, ~4,000 learners across the whole
             textbook catalogue — every book with a committed learning graph.

WHAT IS REAL
------------
Concepts, prerequisite edges, taxonomy categories, chapter names, textbook titles and
MicroSim titles all come from the textbook repos. Learners, districts, schools,
sections, pages, quizzes and every mastery score are synthesised here.

Grade placement is NOT modelled. Schools are all high schools and books are assigned
to them round-robin, because the source learning graphs carry no book-level grade
metadata — 24 of 53 books have no grade signal at all, and what exists is malformed in
several. Inventing a grade band per book would put a plausible-looking lie in the
graph, so the demo declines to have an opinion.

WHAT THIS IS NOT
----------------
Design §8.7 seeds synthetic *statements* and lets the pipeline compress them into the
graph. That path does not exist: `processor` and `summarizer` are both unbuilt (cli.py
line 8), so nothing can turn a statement log into a summary vertex. This module writes
the summary vertices DIRECTLY, deviating from spec §4.3's "projections, always
reproducible by replaying the immutable statement log". These are reproducible only by
re-running this seeder.

That is why every node here carries `seeded: true` — it keeps the demo distinguishable
from a real projection by query rather than by memory, and makes the dataset removable
in one pass (`--clear`). When the summarizer lands, the honest migration is `--clear`
and replay, not reconciliation.

C-1 is respected: no `:Statement` vertex is created, ever.

WHY MASTERY IS NOT RANDOM
-------------------------
`mastery_score` is generated by propagating each learner's ability DOWN the DEPENDS_ON
DAG: a concept's expected mastery is scaled by how well its prerequisites went. Some
learners are given an explicit `gap` — a high-fan-out concept they never got — and the
damage flows to everything downstream of it.

This matters because it is the whole demo. Against uniform-random mastery, "which
unmastered prerequisite is blocking this student?" returns noise: a learner can fail
Osmosis while having aced Diffusion, and the graph is a pretty picture that answers
nothing. With propagation, the question has a real answer and the graph earns its
place. The data has to have the structure you claim to be able to find in it.
"""

